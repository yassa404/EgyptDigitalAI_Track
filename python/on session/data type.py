# type () bilt in function to show dat type
#---------------------
print (type(5))                                 #'int'
print (type(5.55))                              #'float'
print (type("yassa"))                           #'str'
print (type([1 ,2 ,22]))                        #'list'
print (type((1 ,2 ,22)))                        #'tuple'
print (type({"yassa":1,"two":2,"three":3}))     #'dict'
print (type(5==5))                              #'bool'